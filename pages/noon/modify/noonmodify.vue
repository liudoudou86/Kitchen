<template>
	<view class="form">
		<view class="uni-padding-wrap uni-common-mt">
			<form @submit="formSubmit" @reset="formReset">
				<view class="uni-form-item uni-column">
					<view class="title">种类：</view>
					<radio-group name="type">
						<label>
							<radio value="noonfood1" /><text>主菜①</text>
						</label>
						<label>
							<radio value="noonfood3" /><text>主菜②</text>
						</label>
						<label>
							<radio value="noonfood3" /><text>主菜③</text>
						</label>
						<label>
							<radio value="noonfood4" /><text>主菜④</text>
						</label>
						<label>
							<radio value="noonfood5" /><text>主菜⑤</text>
						</label>
						<label>
							<radio value="noonsoup" /><text>汤</text>
						</label>
						<label>
							<radio value="fruit" /><text>水果</text>
						</label>
						<label>
							<radio value="cold" /><text>凉菜</text>
						</label>
					</radio-group>
					<view class="title">菜名：</view>
					<input class="uni-input" name="nickname" @input="inputNickname" placeholder="请输入菜名" />
					<block v-for="(item, _id) in nickNameList" :key="_id">
						<span>{{item}}</span>
					</block>
				</view>
				<view class="uni-btn-v">
					<button  type="primary" form-type="submit">保存提交</button>
					<button type="warn" form-type="reset">重置页面</button>
				</view>
			</form>
		</view>
	</view>
</template>

<script>
	
	export default {
		data() {
			return {
				nickNameList : []
			}
		},
		onLoad(option) {
			const item = JSON.parse(decodeURIComponent(option.item));
			console.log('上一个页面传递过来的参数对象',item);
			this.objData = item;
		},
		methods: {
			// 读取输入的昵称
			inputNickname: function(e) {
				this.nickname = e.detail.value;
				console.log(this.nickname);
				if (this.nickname === '') {
					return this.nickNameList = [];
				}
				// 调用云函数查询
				uniCloud.callFunction({
					name: "readKitchenQuery",
					data: {
						nickname : this.nickname
					}
				}).then((res) => {
					console.log(res);
					const rd = res.result.data;
					for (let i=0; i< rd.length; i++) {
						this.nickNameList.push(rd[i].nickname);
					}
					console.log('模糊查询列表',this.nickNameList);
				}).catch((err) => {
					console.log(err);
				});
			},
			formSubmit: function(e) {
				const that = this;
				const formData = e.detail.value;
				console.log('提交表单数据：' + JSON.stringify(e.detail.value));
				// 调用云函数向云数据库插入数据
				uniCloud.callFunction({
					name: "updateNoonMaterialsData",
					data: {
						date : that.objData.date,
						type : formData.type,
						noonfood1 : that.objData.noonfood1,
						noonfood2 : that.objData.noonfood2,
						noonfood3 : that.objData.noonfood3,
						noonfood4 : that.objData.noonfood4,
						noonfood5 : that.objData.noonfood5,
						noonsoup : that.objData.noonsoup,
						fruit : that.objData.fruit,
						cold :  that.objData.cold,
						oldNickname : that.objData.nickname,
						nickname : formData.nickname,
					}
				}).then((res) => {
					console.log(res);
					uni.hideLoading()
					uni.showModal({
						content: "保存成功",
						showCancel: false
					})
				}).catch((err) =>{
					console.log(err);
					uni.hideLoading()
					uni.showModal({
						content: "保存失败",
						showCancel: false
					})
				});
			},
			formReset: function(e) {
				console.log('清空数据')
			}
		}
	}
	
</script>

